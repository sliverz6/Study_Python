##### 3-8 세계 둘러보기 #####
wish_place = ['iceland', 'vegas', 'afreca', 'amazon', 'newyork']
print(wish_place)
print(sorted(wish_place))
print(wish_place)
print(sorted(wish_place, reverse=True))
print(wish_place)

wish_place.reverse()
print(wish_place)
wish_place.reverse()
print(wish_place)

wish_place.sort()
print(wish_place)

wish_place.sort(reverse=True)
print(wish_place)

print(len(wish_place))